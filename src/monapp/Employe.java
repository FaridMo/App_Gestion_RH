    public void recuperation() {
        try {
            SimpleDateFormat sdf = new SimpleDateFormat("dd-MM-yyyy");
            String daterecup = sdf.format(dateNaissance.getDate());
            String daterecup1 = sdf.format(dateEmbauche.getDate());
            Country selectedCountry = (Country) lieuNaissance.getSelectedItem();

            nomEmp = nom.getText().toUpperCase().trim();
            prenomEmp = prenom.getText();
            num_cni = cni.getText().trim();
            sexeEmp = sexe.getSelectedItem().toString();
            dateNaissanceEmp = daterecup;
            lieuNaissanceEmp = selectedCountry.toString();
            grpSanguinEmp = groupeSanguin.getSelectedItem().toString();
            etudeEmp = niveauEtude.getSelectedItem().toString();
            departementEmp = departement.getText();
            posteEmp = poste.getText();
            typeContratEmp = typeContrat.getSelectedItem().toString();
            salaireEmp = salaire.getText();
            embauche = daterecup1;
            telMobileEmp = telMobile.getText();
            telFixeEmp = telFixe.getText();
            emailEmp = email.getText();
        } catch (NullPointerException e) {
            JOptionPane.showMessageDialog(this, "Saisir la date de naissance et la date d'embauche SVP !");
        }

    }

