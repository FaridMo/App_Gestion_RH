    private void ajouterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ajouterActionPerformed

        recuperation();

        String[] colonnes = {"nom", "prenom", "num_cni", "sexe", "dateNais", "lieuNais", "grpsanguin", "etude", "departement", "poste", "contrat", "salaire", "date_embauche", "telMobile", "telFixe", "email"};
        String[] values = {nomEmp, prenomEmp, num_cni, sexeEmp, dateNaissanceEmp, lieuNaissanceEmp, grpSanguinEmp, etudeEmp, departementEmp,
            posteEmp, typeContratEmp, salaireEmp, embauche, telMobileEmp, telFixeEmp, emailEmp};
        res = selectCondition(table, num_cni);

        if (!nomEmp.isEmpty() && !prenomEmp.equals("") && !num_cni.equals("") && sexe.getSelectedIndex() != 0 && !dateNaissanceEmp.equals("")
                && !posteEmp.equals("") && !telMobileEmp.equals("") && !salaireEmp.equals("") && !emailEmp.equals("")
                && lieuNaissance.getSelectedIndex() != 0 && groupeSanguin.getSelectedIndex() != 0 && niveauEtude.getSelectedIndex() != 0 && !departementEmp.equals("")
                && typeContrat.getSelectedIndex() != 0 && !telFixeEmp.equals("") && !embauche.equals("")) {

            if (dateEmbauche.getDate().after(dateNaissance.getDate())) {
                insert("employe", colonnes, values);
                System.out.println("employé ajouté");
            }else{
                JOptionPane.showMessageDialog(this, "La date d'embauche doit ête supérieur à la date de naissance !!");
            }

        } else {
            JOptionPane.showMessageDialog(this, "Veuillez remplir tous les champs !");
        }
        actualiser();
        table();
    }//GEN-LAST:event_ajouterActionPerformed

