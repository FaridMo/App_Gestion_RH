    private void maTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_maTableMouseClicked
        DefaultTableModel model = (DefaultTableModel) maTable.getModel();

        int selectedRowIndex = maTable.getSelectedRow();
        Date date = null, date1 = null, date2 = null;
        supprimer.setEnabled(true);
        modifier.setEnabled(true);
        try {

            nom.setText(model.getValueAt(selectedRowIndex, 1).toString());
            prenom.setText(model.getValueAt(selectedRowIndex, 2).toString());
            date = new SimpleDateFormat("dd-MM-yyyy").parse((String) model.getValueAt(selectedRowIndex, 3));
            dateEmb.setDate(date);
            typeContrat.setSelectedItem(model.getValueAt(selectedRowIndex, 4));
            poste.setText(model.getValueAt(selectedRowIndex, 5).toString());
            date1 = new SimpleDateFormat("dd-MM-yyyy").parse((String) model.getValueAt(selectedRowIndex, 6));
            debutConge.setDate(date1);
            date2 = new SimpleDateFormat("dd-MM-yyyy").parse((String) model.getValueAt(selectedRowIndex, 7));
            finConge.setDate(date2);
            typeConge.setSelectedItem(model.getValueAt(selectedRowIndex, 8).toString());
        } catch (ParseException ex) {
            System.out.println("");
        } catch (NullPointerException e) {
            System.out.println("");
        }

    }//GEN-LAST:event_maTableMouseClicked

