    public void recuperation() {
        try {
            SimpleDateFormat sdf = new SimpleDateFormat("dd-MM-yyyy");
            String daterecup = sdf.format(dateEmb.getDate());
            String daterecup1 = sdf.format(debutConge.getDate());
            String daterecup2 = sdf.format(finConge.getDate());

            nomEmp = nom.getText().toUpperCase().trim();
            prenomEmp = prenom.getText();
            dateEmbaucheEmp = daterecup;
            typeContratEmp = typeContrat.getSelectedItem().toString();
            posteEmp = poste.getText();
            Conge = typeConge.getSelectedItem().toString();
            dateDebutConge = daterecup1;
            dateFinConge = daterecup2;
        } catch (NullPointerException e) {
            JOptionPane.showMessageDialog(this, "Saisissez bien les dates !");
        }

    }

