    private void modifierActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_modifierActionPerformed
        DefaultTableModel model = (DefaultTableModel) maTable.getModel();

        recuperation();

        String[] colonnes = {"nom", "prenom", "date_embauche", "contrat", "poste", "debut_conge", "fin_conge", "type_conge"};

        String[] valeurs = {nomEmp, prenomEmp, dateEmbaucheEmp, typeContratEmp, posteEmp,
            dateDebutConge, dateFinConge, Conge};

        String cni = String.valueOf(model.getValueAt(maTable.getSelectedRow(), 0));
        try {
            if (debutConge.getDate().after(dateEmb.getDate())) {
                if (finConge.getDate().after(debutConge.getDate())) {

                    updateTable(table, colonnes, valeurs, cni);

                } else {
                    JOptionPane.showMessageDialog(this, "La date de fin du congé doit être supérieur à la date de début du congé");
                }
            } else {
                JOptionPane.showMessageDialog(this, "La date de début congé doit être supérieur à la date d'embauche");
            }
        } catch (ArrayIndexOutOfBoundsException e) {
            System.out.println("");
        }

        actualiser();
        tableConge();
    }//GEN-LAST:event_modifierActionPerformed

    private void supprimerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_supprimerActionPerformed
        DefaultTableModel model = (DefaultTableModel) maTable.getModel();
        int ligne = maTable.getSelectedRow();
        String cni = String.valueOf(model.getValueAt(ligne, 0));
        if (maTable.getSelectedRowCount() == 1) {
            supprimer.setEnabled(true);
            deleteColumn(table, cni);

            if (maTable.getRowCount() == 0) {
                JOptionPane.showMessageDialog(this, "Table vide !");
            }
        } else {
            supprimer.setEnabled(true);
        }

        actualiser();
        tableConge();
    }//GEN-LAST:event_supprimerActionPerformed

